def generate_jagged_list(n, fill_char):
    """
    Генерує зубчатий список, що містить лише заштриховані елементи
    (верхню ліву трикутну частину) матриці розміром n x n.

    Параметри:
        n (int): Розмір квадратної матриці.
        fill_char (str): Символ для заповнення матриці.

    Повертає:
        list: Зубчатий список, що містить заштриховані елементи.
    """
    jagged_list = []
    for i in range(n):
        # Генеруємо рядок, де перші i+1 елементів — заштриховані, а решта — порожні
        row = [fill_char] * (n - i) + [' '] * i
        jagged_list.append(row)
    return jagged_list


def main():
    # Введення розміру матриці
    try:
        n = int(input("Введіть розмір квадратної матриці: "))
        if n <= 0:
            print("Розмір матриці має бути додатнім цілим числом.")
            return
    except ValueError:
        print("Некоректне введення. Будь ласка, введіть ціле число для розміру матриці.")
        return

    # Введення символу для заповнення
    fill_char = input("Введіть один символ для заповнення матриці: ")
    if len(fill_char) != 1:
        print("Будь ласка, введіть один символ для заповнення.")
        return

    # Генерація зубчатого списку
    jagged_list = generate_jagged_list(n, fill_char)

    # Виведення результату
    print("Згенерований зубчатий список:")
    for row in jagged_list:
        print(" ".join(row))


# Виконання основної функції
if __name__ == "__main__":
    main()
